// Teste de declaração de variáveis
int main() {
    // Declaração simples sem inicialização
    int a;
    float b;
    char c;
    bool d;

    // Declaração simples com inicialização
    int x = 10;
    float y = 2.5;
    char z = 'w';
    bool flag = true;

    // Declaração múltipla sem inicialização
    int m, n, o;
    float f1, f2;
    char ch1, ch2;
    bool eh_vdd, n_sei;

    // Declaração múltipla com inicialização
    int p = 1, q = 2, r = 3;
    float g = 3.14, h = 0.5;
    char letra1 = 'a', letra2 = 'b';
    bool ativo = false, pronto = true;

    // Declaração múltipla misturando inicializadas e não inicializadas
    int s, t = 20, u;
    float i = 1.1, j, k = 2.2;
    char l, m2 = 'x', n2;
    bool b1, b2 = true, b3;

    // Teste de uso após declaração
    a = 5;
    b = 7.5;
    c = 'z';
    d = false;

    return 0;
}